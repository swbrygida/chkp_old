  <p>Czym jest więc naród? To zbiorowość ludzi na danym obszarze, którą łączy wspólne państwo, świadomość narodowa, język i kultura, czyli poczucie przynależności do wspólnoty definiowanej jako naród. Co to jest świadomość narodowa? Polega ona na tym, że poszczególni członkowie danej zbiorowości odczuwają przynależność narodową i kojarzą swoje losy osobiste z losami narodu, za który czują się odpowiedzialni i w momencie zagrożenia zewnętrznego solidarnie działają w jego obronie. Na świadomość narodową składa się także wiedza na temat wspólnej przeszłości historycznej, język i kultura narodowa, która dla większości członków tej wspólnoty jest przedmiotem dumy i znakiem rozpoznawalnym na tle innych narodów. Czym jest sprawa narodowa? To nasza Ojczyzna, tożsamość narodowa, nasz język, kultura, zwyczaje, wiara. Świadomość narodowa to przede wszystkim własne państwo lub przynajmniej wyobrażenie o nim. Takiego wyobrażenia własnego państwa jako naród doświadczyliśmy w okresie zaborów.</p>
  <p>Rozbiory Polski, które miały miejsce w końcu XVIII w. były wyrazem ekspansjonistycznej polityki mocarstw ościennych, w której dominującą rolę odegrała Rosja. W wyniku porozumienia się Rosji, Austrii i Niemiec państwo polskie o wielowiekowych dziejach i bogatej kulturze na przeszło 100 lat przestało istnieć. I chociaż Polska zniknęła z map Europy, to jednak duch narodu nie zginął. Jak podkreślił Jan Paweł II podczas swojego przemówienia w siedzibie UNESCO w Paryżu w czerwcu 1980 r., Polska to naród który przetrwał najstraszliwsze doświadczenie dziejów, który przez sąsiadów został skazany na śmierć, i który mimo wszystko przetrwał i pozostał sobą, ponieważ zachował własna kulturę, pamięć i wolę przetrwania.</p>
