  <p>Kościół Pobrygidkowski – ośrodkiem pielęgnowania pamięci historyczne narodu.</p>
  <p>Zainicjowane przez ks. Mieczysława Brzozowskiego wieczorne msze święte za Ojczyznę od 1976 roku gromadziły w tym kościele liczne rzesze przedstawicieli lubelskiej inteligencji oraz kombatantów – byłych żołnierzy Polskiej Armii Podziemnej: Armii Krajowej, Batalionów Chłopskich, Zrzeszenia Wolność i Niezawisłość oraz Więźniów Politycznych, Związku Sybiraków i Rodziny Katyńskiej, jak również patriotycznej młodzieży.</p>
  <p>Magnesem przyciągającym wiernych na te religijno-patriotyczne nabożeństwa były rocznice ważnych wydarzeń historycznych i przypominanie naszych bohaterów narodowych, wzbogacane wykładami z historii, prelekcjami i recytacją poezji patriotycznej oraz pieśniami religijno-patriotycznymi.</p>
  <p>W okresie komunistycznego zniewolenia (1945-1989) świątynia ta była azylem patriotycznego społeczeństwa Lublina, które tu mogło słuchać – nie zakłamanej przez władze komunistyczne PRL – historii Polski i losów Narodu, w tym również o bohaterach i męczennikach narodowej sprawy.</p>
  <p>Inicjatywę tę przejęli i kontynuowali kolejni rektorzy, a formalnie, oficjalne klepsydry o mszach świętych rocznicowych – od końca lat 80-tych XX w. redagowali i rozklejali na kościołach przedstawiciele powstałych po 4. 06. 1989 r. organizacji kombatanckich – Związku Żołnierzy Armii Krajowej i Stowarzyszenia Polskich Kombatantów w Kraju – Oddział Wojewódzki w Lublinie, wieloletni inicjator i organizator.</p>
  <p>Wprowadzeniem do mszy świętych stały się wstępy historyczne i recytacje związanych z danym wydarzeniem wierszy oraz odczytywanie wezwań modlitwy wiernych, zawierające odniesienia do wydarzeń i bohaterów narodowych, jak: marszałkowie Polski Józef Piłsudski i Edward Rydz – Śmigły, gen. Władysław Sikorski.</p>
