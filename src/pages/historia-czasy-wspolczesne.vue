  <p>Wymienione imiennie inne osoby – obok równie zasłużonych, ale mniej znanych szeregowych bohaterów i męczenników narodowej sprawy – dokumentują szczególny charakter Kościoła Pobrygidkowskiego jak swego rodzaju Mauzoleum najnowszej historii Polski.</p>
