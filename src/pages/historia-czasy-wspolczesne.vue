  <p>Prześladowania jakich doświadczaliśmy jako naród od końca XVIII w. dotyczyły także zakorzenionej od wieków wiary katolickiej i Kościoła. W tragicznym okresie niewoli narodowej Kościół katolicki stawał się przestrzenią inicjatywy narodowej, jego ostoją i miejscem walki o godność narodu i człowieka. Skąd wychodziła inspiracja do walki o wszystkie wartości budujące polską tożsamość narodową i religijną. Kościół katolicki w trudnych dziejach Polski – pod zaborami, okupacją niemiecką i komunistyczną – stał się ostoją nadziei w czasach bez nadziei. Wiedział o tym gubernator Hans Frank, pisząc: „Kościół Katolicki i naród polski to dwie ręce złożone do modlitwy. Będziemy uderzać raz w jedną, raz w drugą, aż je zniszczymy”. Wiedział o tym także Stalin, dlatego wydał on podległym sobie władzom Polski Ludowej odpowiednie dyrektywy. Stąd tak bezwzględna walka zaborców i okupantów, a nawet rodzimych zdrajców zaprzedanych wrogom narodu, z religią i Kościołem. Za swą wierność najwyższym wartościom w okresie komunizmu społeczeństwo polskie, na czele z błogosławionym Prymasem Tysiąclecia, kard. Stefanem Wyszyńskim płaciło wysoką cenę. Za wierność sprawie narodowej i wierze katolickiej oprawcy odwdzięczali się więzieniem, zsyłką do obozów a często śmiercią.</p>
  <p>Kościół Wniebowzięcia NMP Zwycięskiej w Lublinie</p>
  <p>Pod takim tytułem powstała w 1996 roku syntetyczna historia Kościoła Pobrygidkowskiego w Lublinie z sześćsetną rocznicę poświęcenia pierwszej kaplicy Najświętszej Maryi Panny, św. Barbary i św. Zofii.</p>
  <p>Kościół został wzniesiony w latach 1412-26 jako wotum dziękczynne króla Władysława Jagiełły za zwycięstwo w bitwie pod Grunwaldem. Informuje o tym tablica pod popiersiem króla Władysława Jagiełły w Kaplicy Grunwaldzkiej, umieszczona 15 lipca 1910 roku – w pięćsetną rocznicę zwycięstwa nad Zakonem Krzyżackim, upamiętnioną również zewnętrznym głazem – pomnikiem tego historycznego wydarzenia. Po tych pierwszych – w kolejnych latach minionego XX wieku – powstały dziesiątki innych.</p>
  <p>O budowie tego kościoła informują ponadto dwie tablice: nad głównym wejściem z 1945 r. i w niszy na frontonie kościoła z 1970 r.</p>
  <p>Po wyparciu Rosjan z Lublina i po zwycięskiej bitwie pod Jastkowem w 1915 roku w dniu 22 stycznia 1916 roku podczas obchodów 53. Rocznicy Powstania Styczniowego 1863 wmurowana została w prawej nawie tablica pamiątkowa o treści: „Cześć walczącym o wolność i niepodległość Ojczyzny”. Obecnie tablica została umieszczona na ekspozycji pod hasłem „Jeśli zapomnę o nich, Ty, Boże na niebie zapomnij o mnie” w wieży kościoła”.</p>
