  <p>Kościół Wniebowzięcia NMP Zwycięskiej w Lublinie oprócz zasadniczej swej roli duszpasterskiej – głoszenia Słowa Bożego, odprawiania mszy św. i nabożeństw i udzielania sakramentów świętych – posiada również poważne zasługi jako ośrodek społeczno – wychowawczy, kulturotwórczy i oświatowy. W latach rządów komunistycznych wyświetlane były tu filmy o tematyce biblijnej, religijnej i historycznej, które ze względu na swą treść nie mogły wówczas być prezentowane w objętych cenzurą kinach (np. Jezus z Nazaretu”, „Gologota”, „Sodoma i Gomora”, „Quo vadis” i inne).</p>
  <p>W czasie rządów rektora ks. kan. dra S. Młynarczyka organizowane były w kościele konferencje o tematyce religijnej, historycznej i światopoglądowej.</p>
