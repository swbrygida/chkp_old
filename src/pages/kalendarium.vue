    </p></section>
    <section><p>

    1874 r. – w Lublinie zawiązało się bractwo Nieustającej Adoracji Najświętszego Sakramentu, dla którego w kościele Wniebowzięcia NMP Zwycięskiej była odprawiana msza św. w pierwszy piątek każdego miesiąca.
    </p></section>
