    </p></section>
    <section><p>

    1510 r. – klasztor nabył nową posiadłość ziemską. Niejaki Bartłomiej albo Bartko, obywatel lubelski, będąc właścicielem sześciu łanów pola pod miastem Lublinem, uzyskał w r. 1396 od króla Władysława Jagiełły z powodu swych zasług przywilej zwalniający go i jego potomków od płacenia z tych łanów podatku i wszelkich ciężarów publicznych. Potomek jego Mikołaj, obywatel lubelski za szczególne zasługi wyjednał sobie zatwierdzenie tego przywileju w 1439 r. od Władysława III Wareńczyka. Klasztor z tych sześciu łanów nabył 3 łany, pozostałe 3 dokupił w początkach XVI w.

    Ksieni Agnieszka Jastkowska zakupiła z posagów wnoszonych przez kandydatki do zakonu wieś Minkowice dolne i górne z folwarkiem.

    Klasztor miał liczne posiadłości ziemskie oraz czynsze roczne z zapisu królów lub osób prywatnych. Najwięcej przywilejów konfirmacyjnych udzielił klasztorowi Zygmunt Stary, Stefan Batory i Zygmunt II.
    </p></section>
    <section><p>

    4 lipca 1661 r. – Jan Kazimierz zatwierdza przywileje klasztoru Brygidek w Lublinie, które zostały spisane w formie księgi. Po zaginięciu oryginałów nadawanych przywilejów ta księga stała się główną podstawą prawną.
    </p></section>
    <section><p>

    Od końca XVI w. prowadzono prace renowacyjne w kościele, dobudowano nowy klasztor i mury okalające posiadłość. Wieżę przy frontonie dobudowano za czasów rządów dwóch ksieni klasztoru: Agnieszki Jastkowskiej (1589-1630) i Doroty Firlejówny (1632-1660).

    Pierwsza z nich gorliwa reformatorka zmurowała 3 części klasztoru, mieszkanie dla Gertrudy z Opalińskich oraz groby dla zakonnic w kryptach, a Dorota Firlejówna przeprowadziła parce modernizacyjne w kościele.
    </p></section>
    <section><p>

    Rządy ksieni Agnieszki Jastkowskiej rozpoczęły nowy okres w dziejach klasztoru i były czasem jego najświetniejszego rozwoju zarówno pod względem materialnym jak i moralnym. Ponieważ z powodu usunięcia zakonników służba Boża w kościele się zmniejszyła, ksieni zorganizowała fundusz na utrzymanie 4 kapłanów oraz kaznodziei, spowiednika, kantora, organisty i zakrystianina. Klasztor lubelski zyskał wielki rozgłos. Wstępowały do niego córki z najpierwszych i najmożniejszych domów polskich.
    </p></section>
    <section><p>

    W XVII w. założono klasztory Brygidek we Lwowie, Samborze, Sokalu, Pipiu, Warszawie, Brześciu, Łucku i Grodnie. Ksieni lubelskiego klasztoru wysyłała na te nowe fundacje zakonnice celem zorganizowania ich i urządzenia według reformy przeprowadzonej w lubelskim klasztorze.
    </p></section>
    <section><p>

    Rządy ksieni Doroty Firlejówny były do roku 1648 dalszym ciągiem pomyślnego pod każdym względem bytu klasztoru Brygidek w Lublinie. Dzięki jej zaangażowaniu wiele dziewcząt najzamożniejszych domów polskich zapragnęło wstąpić do lubelskiego klasztoru. Dokończyła budowę klasztoru i cały klasztor wygodnie i ozdobnie urządziła. Postarała się o odzyskanie niektórych posiadłości klasztornych.

    Szczególnie zajmowała się ozdobą świątyni pańskiej, w której prowadziła prace restauratorskie, ozdabiała ołtarze, obrazy, naczynia i szaty kościelne. W czasach Jana Kazimierza zmuszona była trzykrotnie uciekać z zakonnicami ze swego klasztoru i szukać schronienia.
    </p></section>
    <section><p>

    W XVI w. po reformie klasztoru zostały w lubelskim konwencie tylko same siostry. Każda ksieni otaczała wielką troską zakrystię i skarbiec.
    </p></section>
    <section><p>

    Od połowy XVI w. oprócz przeorów w sprawach ważniejszych występują także ksieni. Po usunięciu z klasztoru zakonników ksieni zarządzała klasztorem w występowała w jego imieniu.
    </p></section>
    <section><p>

