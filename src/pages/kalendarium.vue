    </p></section>
    <section><p>

    9 stycznia 1649 r. – ksieni wraz z Siostrami powróciły do lubelskiego klasztoru. W czasie tułaczki urządzała porządek życia klasztornego, a swoją cierpliwością w znoszeniu trudów i niedostatku budowała siostry i postępowała wobec nich z prawdziwie matczyną miłością i troskliwością.
    </p></section>
    <section><p>

