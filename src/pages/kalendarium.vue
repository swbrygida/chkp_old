    </p></section>
    <section><p>

    1791-1814 – czasy ksieni Teofili Kuczewskiej i 1814-1818 ksieni Maryanny Milkiewiczówny. Klasztor powoli chyli się ku upadkowi. Rząd Księstwa Warszawskiego wprowadził ograniczenia w przyjmowaniu nowych panien do klasztoru, a to wpłynęło na zmniejszenie się liczby zakonnic.
