    </p></section>
    <section><p>

    1708-1719 rządy ksieni Teofilii Gołuchowskiej. Po potopie Szwedzkim w kraju trwał wielki niepokój. Po powrocie do klasztoru przeprowadziła prace przy kościele, sprofanowanych grobach i zabudowaniach klasztornych.
    </p></section>
    <section><p>

    1720-1737 – rządy ksieni Teofili Dłużewskiej. Pożar zniszczył 3 części klasztoru, który został odrestaurowany za rządów ksieni. Po okresie zawirowań społeczno-politycznych w kraju ksieni odnowiła zasady życia klasztornego i wspólnotowego.
    </p></section>
    <section><p>

    1737-1767 – rządy ksieni Rozalii Swęderskiej. Za jej rządów klasztor lubelski cieszył się bardzo dobrą opinią wśród społeczeństwa i przełożonych.
    </p></section>
    <section><p>

